package aula16;

public class Loops {

	public static void main(String[] args) {

		int count = 5;
		int limit = 0;

		while (count > limit){
			System.out.println("Passo " + count);
			count --;
		}
		System.out.println("---");
		for (int i=0; i < 5; i++){

			System.out.println("Passo " +i);

		}

	}
}

